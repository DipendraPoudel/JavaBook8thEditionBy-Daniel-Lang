public class SwappNumbersExample {
    
    public static void main(String [] args){
        int a = 5;
        int b =4;
        int temp;
        
        // swapping
        temp = a;  //this initially holds the value of a and store it in temp variable and later we use in line 22
        a = b;
        b = temp;
             
        
        
        
    
    
    System.out.println("The value of a:\t"+a);
    System.out.println("The value of b:\t"+b);
    
    }
    
    
    
    
}
